package com.example.uilearning.canvas_transform;

public class Ball {

    /**
     * 小球像素点颜色
     */
    public int color;
    /**
     * 小球圆心x坐标
     */
    public float x;
    /**
     * 小球圆心y坐标
     */
    public float y;
    /**
     * 小球半径
     */
    public float r;

    /**
     * X速度
     */
    public float vX;
    /**
     * Y速度
     */
    public float vY;

    /**
     * 水平加速度
     */
    public float aX;
    /**
     * 垂直加速度
     */
    public float aY;
}
